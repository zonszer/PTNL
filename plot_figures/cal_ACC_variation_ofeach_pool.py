import re

def parse_pool_info(text):
    pool_info = {}
    lines = text.split('\n')
    for line in lines:
        if 'pool_id' in line:
            pool_id = int(re.search(r'pool_id: (\d+)', line).group(1))
            pool_acc = tuple(map(int, re.search(r'pool ACC: (\d+)/(\d+)', line).groups()))
            pool_info[pool_id] = pool_acc
    return pool_info

def compare_pool_info(info_before, info_after):
    changes = {}
    for pool_id, acc_before in info_before.items():
        acc_after = info_after.get(pool_id)
        if acc_after is not None and acc_before != acc_after:
            changes[pool_id] = (acc_before, acc_after)
    return changes

def total_acc(info):
    total_numerator = sum(acc[0] for acc in info.values())
    total_denominator = sum(acc[1] for acc in info.values())
    return total_numerator, total_denominator

def track_pool_info(text):
    text_info = text.split('====>')
    pool_info = {}
    for text in text_info:
        if 'pool_id' in text:  # Check if the text contains pool information
            info = parse_pool_info(text)
            for pool_id, acc in info.items():
                if pool_id not in pool_info:
                    pool_info[pool_id] = []
                pool_info[pool_id].append(acc)
    return pool_info

def print_pool_info(pool_info):
    for pool_id, accs in pool_info.items():
        # Check if all ACC values are the same
        if all(acc == accs[0] for acc in accs):
            continue  # Skip this pool if there are no changes
        accs_str = ' --> '.join([f'{acc[0]}/{acc[1]}' for acc in accs])
        print(f'Pool {pool_id} ACC changed: {accs_str}')

def print_total_acc(text):
    text_info = text.split('====>')
    total_accs = [total_acc(parse_pool_info(text)) for text in text_info if 'pool_id' in text]
    total_accs_str = ' --> '.join([f'{acc[0]}/{acc[1]}' for acc in total_accs])
    print(f'Total ACC changed: {total_accs_str}')


